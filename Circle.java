package za.wsu.ac.s2190328087;

public class Circle {
	private double radius;
	private int x;
	private int y;
	
	public void setRadius(double radius) {
		this.radius = radius;
	}
	
	public void setX(int x) {
		this.x = x;
	}
	
	public void setY(int y) {
		this.y = y;
	}
	
	public double getRadius() {
		return radius;
	}
	
	public int getX() {
		return x;
	}
	
	public int getY() {
		return y;
	}
	
}
